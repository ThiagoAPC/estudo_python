#Tipo de dado INTEIRO
n = int(input('Digite algo: '))
print(n)

#Tipo de dado em Ponto Flutuante
m = float(input('Digite algo: '))
print(m)

#Tipo de dado em String
o = str(input('Digite algo: '))
print(o)

#Tipo de dado em Boolean
p = bool(input('Digite algo: '))
print(p)

#Reconhecimento numérico
q = input('Digite algo: ')
print(q.isnumeric())

#Reconhecimento alfabético
q = input('Digite algo: ')
print(q.isalpha())

#Reconhecimento alfanumérico
q = input('Digite algo: ')
print(q.isalnum())

#Reconhecimento de Upper Case
q = input('Digite algo: ')
print(q.isupper())

#Reconhecimento de Lower Case
q = input('Digite algo: ')
print(q.islower())